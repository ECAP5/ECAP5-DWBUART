#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include <verilated.h>
#include <verilated_vcd_c.h>
#include <svdpi.h>

#include "Vtb_ecap5_wbuart.h"
#include "testbench.h"

enum CondId {
  __CondIdEnd
};

enum TestcaseId {
};

enum StateId {
};

class TB_Ecap5_wbuart : public Testbench<Vtb_ecap5_wbuart> {
public:
  void reset() {
    this->_nop();

    this->core->rst_i = 1;
    for(int i = 0; i < 5; i++) {
      this->tick();
    }
    this->core->rst_i = 0;

    Testbench<Vtb_ecap5_wbuart>::reset();
  }
  
  void _nop() {
  }
};

int main(int argc, char ** argv, char ** env) {
  srand(time(NULL));
  Verilated::traceEverOn(true);

  bool verbose = parse_verbose(argc, argv);

  TB_Ecap5_wbuart * tb = new TB_Ecap5_wbuart;
  tb->open_trace("waves/ecap5_wbuart.vcd");
  tb->open_testdata("testdata/ecap5_wbuart.csv");
  tb->set_debug_log(verbose);
  tb->init_conditions(__CondIdEnd);

  /************************************************************/

  tb->tick();
  tb->tick();
  tb->tick();
  tb->tick();

  /************************************************************/

  printf("[ECAP5_WBUART]: ");
  if(tb->success) {
    printf("Done\n");
  } else {
    printf("Failed\n");
  }

  delete tb;
  exit(EXIT_SUCCESS);
}
